def main():
    create_tables()
    session = get_session()
    try:
        session.execute(delete(SearchKeywordRule))
        session.execute(delete(TargetProductIdRule))
        session.execute(delete(PriceRule))
        session.execute(delete(Item))
        session.execute(delete(Mall))
        session.commit()

        mall_map = {}
        for idx, mall_name in enumerate(target_malls, start=1):
            mall = Mall(
                mall_name=mall_name,
                mall_display_name=mall_display_names.get(mall_name, mall_name),
                enabled=True,
                sort_order=idx,
            )
            session.add(mall)
            session.flush()
            mall_map[mall_name] = mall

        item_names = list(search_keywords.keys())
        item_map = {}
        for idx, item_name in enumerate(item_names, start=1):
            item = Item(display_name=item_name, enabled=True, sort_order=idx)
            session.add(item)
            session.flush()
            item_map[item_name] = item

        for item_name, mall_rules in search_keywords.items():
            for mall_name, keyword in mall_rules.items():
                session.add(SearchKeywordRule(
                    item_id=item_map[item_name].id,
                    mall_id=mall_map[mall_name].id,
                    search_keyword=keyword,
                ))

        for item_name, mall_rules in target_product_ids.items():
            if item_name not in item_map:
                continue
            for mall_name, target_id in mall_rules.items():
                if mall_name not in mall_map:
                    continue
                session.add(TargetProductIdRule(
                    item_id=item_map[item_name].id,
                    mall_id=mall_map[mall_name].id,
                    target_product_id=target_id,
                ))

        for item_name, mall_rules in price_rules.items():
            if item_name not in item_map:
                continue
            for mall_name, rule in mall_rules.items():
                if mall_name not in mall_map:
                    continue
                op, value = rule
                session.add(PriceRule(
                    item_id=item_map[item_name].id,
                    mall_id=mall_map[mall_name].id,
                    op=op,
                    value=float(value),
                ))

        session.commit()
        print('초기 데이터 입력 완료')
    finally:
        session.close()


if __name__ == '__main__':
    main()
